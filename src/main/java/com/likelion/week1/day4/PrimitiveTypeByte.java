package com.likelion.week1.day4;

public class PrimitiveTypeByte {
		public static void main(String[] args) {
				// Byte Range => -128 ~ 127 까지 할당가능
				byte byte1 = 10;
				byte byte2 = -127;
				//byte byte3 = -129; // -128, 128 은 할당되지 않음!
				//byte byte4 = 128; // -128, 128 은 할당되지 않음!
				System.out.println(byte1);
				System.out.println(byte2);
		}
}
