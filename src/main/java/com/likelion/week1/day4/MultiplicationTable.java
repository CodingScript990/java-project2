package com.likelion.week1.day4;

public class MultiplicationTable {
		public static void main(String[] args) {
				// 2 * 1 = 2
				// 2 * 2 = 4
				// println => 구구단
				System.out.println("println : " + "2" + " * " + "1" + " = " + "2");
				// printf => %d 로 실행
				System.out.printf("printf : %d * %d = %d\n", 2, 1, 2);
				System.out.printf("printf : %d * %d = %d", 2, 2, 4);
		}
}
