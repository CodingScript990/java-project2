package com.likelion.week3.day11;

public class For0to9 {
		public static void main(String[] args) {

				// For statement => Column output
				for (int i = 0; i < 10; i++) { // initial; condition; increasing;
						// output => value[i(0 - 9)]
						System.out.printf("%d\n", i); // [i] : 0 - 9
				}
		}
}
