package com.likelion.week3.day11;

public class ForLoopForward1To10 {
		public static void main(String[] args) {

				// for statement
				for (int i = 1; i <= 10; i++) { // initial; condition; increasing;
						// i는 1이고, i(1)가 10보다 작거나 같으면, 1씩 증가하라!
						// output => values[i]
						System.out.printf("%d\n", i); // 1 2 3 .. 10
						// 10보다 i의 값이 크면? 종료하라!
				}
		}
}
