package com.likelion.week3.day11;

public class ForLoopPrintHorizontal {
		public static void main(String[] args) {

				// for statement => Row output
				for (int i = 0; i < 10; i++) { // initial; condition; increasing;
						// output => value[i(0 - 9)]
						System.out.printf("%d ", i); // Row output : [0 1 2 3 ... 9]
				}
		}
}
