package com.likelion.week2.day7;

public class ReferenceTypeArray {
		public static void main(String[] args) {

				// Input

				// float Array
				float[] fArr = new float [3];

				// String Array
				String[] sArr = new String [3];

				// output

				// new operator[String] 은 null 값이 default 값임
				// new operator[Int] 는 0 값이 default 값임
				System.out.println(sArr[0]);

				// new operator[float] 은 0.0 값이 default 값임
				System.out.println(fArr[0]);



		}
}
