package com.likelion.week2.day8;

public class ArithmeticOperators1 {
		public static void main(String[] args) {

				// int 초기값[a, b]
				int a = 10;
				int b = 3;

				// output => 연산자[+,-,*,/,%%]
				System.out.printf("%d + %d = %d\n", a, b, (a + b));
				System.out.printf("%d - %d = %d\n", a, b, (a - b));
				System.out.printf("%d * %d = %d\n", a, b, (a * b));
				System.out.printf("%d / %d = %d\n", a, b, (a / b));
				System.out.printf("%d %% %d = %d\n", a, b, (a % b));

		}
}
