package com.likelion.week2.day8;

public class LikeAccumulate {
		public static void main(String[] args) {

				// 변수값 누적 하기
				// likeCount variable 초기값
				int likeCount = 0; // 0

				// 대입 연산자
				likeCount += 1; // 0 + 1

				// output
				System.out.println(likeCount); // 1

				// 대입연산자
				likeCount += 1; // 1 + 1

				// output
				System.out.println(likeCount); // 2

				// 대입연산자
				likeCount += 1; // 2 + 1

				// output
				System.out.println(likeCount); // 3
		}
}
