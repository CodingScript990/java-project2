package com.likelion.week2.day10;

public class ElseExam {
		public static void main(String[] args) {

				// boolean type initial value => true
				boolean checkTrue = true;

				// if statement
				if (checkTrue) { // 논리값이 true 인 경우
						System.out.println("true 입니다.");
				} else {
						System.out.println("false 입니다.");
				}

				// boolean type initial value => false
				boolean checkFalse = false;

				// if statement
				if (checkFalse) { // 논리값이 false 인 경우
						System.out.println("true 입니다.");
				} else {
						System.out.println("false 입니다.");
				}
		}
}
