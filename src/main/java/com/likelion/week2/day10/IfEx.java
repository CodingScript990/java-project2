package com.likelion.week2.day10;

public class IfEx {
		public static void main(String[] args) {

				// if statement
				if (true) { // (논리값)
						// 논리값 참(True)
						System.out.println("항상 실행 됩니다.");
				}
				if (false) { // (논리값)
						// 논리값 거짓(False)
						System.out.println("항상 실행 됩니다.");
				}
		}
}
