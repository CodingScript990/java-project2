package com.likelion.week2.day10;

public class OddEven {
		public static void main(String[] args) {

				// int type initial value
				int num = 1;

				// if statement
				if (num % 2 == 0) { // 논리값이 2로 나눈 값이 나머지 0으로 떨어지는가?
						// True 일때
						System.out.println("짝수 입니다.");
				} else {
						// False 일때
						System.out.println("홀수 입니다.");
				}
		}
}
