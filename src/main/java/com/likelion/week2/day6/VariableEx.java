package com.likelion.week2.day6;

// Variable Type class => string, int, float
public class VariableEx {
		public static void main(String[] args) {
				// type setting
				String sOne = new String("1"); // 문자열
				int iOne = 1; // 정수
				float fOne = 1.0f; // 기본 6자리 수로 출력됨

				// Output
				System.out.printf("String:%s Number:%d Float:%f\n", sOne, iOne, fOne);
		}
}
